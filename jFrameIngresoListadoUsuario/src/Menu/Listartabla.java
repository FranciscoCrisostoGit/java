 private void mostrarDatosEnTabla() //metodo para que al inicializar muestre datos automaticamente
    {
        // 1.- Cargar datos en la tabla de usuarios.
        DefaultTableModel dtm = new DefaultTableModel();  //inicio del modelo de la tabla
        
        dtm.addColumn("Nombre");   //añadir las columnas de la tabla
        dtm.addColumn("Apellido");
        dtm.addColumn("Edad");
        dtm.addColumn("Sexo");
        
        for(Usuario u: ListadeUsuarios.lista)  //recorrer nuestra lista para llenar la columna
        {
            Object[] arr = new Object[4]; // 4 columnas => 4 celdas en 'arr'
            
            arr[0] = u.getNombre();
            arr[1] = u.getApellido();
            arr[2] = u.getEdad();
            arr[3] = u.getSexo();
            
            dtm.addRow(arr);        //añadir nuestra columna completa a la tabla
        }
        
        tableUsuarios.setModel(dtm); //toma todo y lo tira al jframe
    }
    
