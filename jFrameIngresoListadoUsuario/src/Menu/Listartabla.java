    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed

        int posFila = tableUsuarios.getSelectedRow(); //obtener la fila seleccionada para borrar

        if(posFila < 0) //si no se selecciona mostrar un mensaje
        {
            JOptionPane.showMessageDialog(null,
                "Antes de eliminar, debe seleccionar un usuario " +
                "de la tabla...");
            return;
        }
        else
        {
            ListadeUsuarios.lista.remove( posFila ); //remover la fila seleccionada
            mostrarDatosEnTabla();
            //JOptionPane.showMessageDialog(null, "Usuario eliminado correctamente!");
        }

    }//GEN-LAST:event_btnEliminarActionPerformed

